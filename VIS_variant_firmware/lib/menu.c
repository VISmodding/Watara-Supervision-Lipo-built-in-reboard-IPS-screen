static void draw_rect(uint8_t *fb, int x, int y, int w, int h, uint8_t color) {
    for (int yy = y; yy < y + h; yy++) {
        for (int xx = x; xx < x + w; xx++) {
            if (xx >= 0 && xx < FRAMEBUFFER_WIDTH && yy >= 0 && yy < FRAMEBUFFER_HEIGHT)
                fb[yy * FRAMEBUFFER_WIDTH + xx] = color;
        }
    }
}

static void draw_char(uint8_t *fb, int x, int y, char c, uint8_t color) {
    if (c > 127) return;
    for (int i = 0; i < 8; i++) {
        uint8_t line = font8x8_basic[(int)c][i];
        for (int j = 0; j < 8; j++) {
            if ((line >> (7 - j)) & 1) { 
                int px = x + j;
                int py = y + i;
                if (px >= 0 && px < FRAMEBUFFER_WIDTH && py >= 0 && py < FRAMEBUFFER_HEIGHT)
                    fb[py * FRAMEBUFFER_WIDTH + px] = color;
            }
        }
    }
}

static void draw_string(uint8_t *fb, int x, int y, const char *s, uint8_t color) {
    while (*s) {
        draw_char(fb, x, y, *s++, color);
        x += 8;
    }
}

// --- Menu behaviour---

void menu_init(void) {
    gpio_init(PIN_UP);
    gpio_set_dir(PIN_UP, GPIO_IN);
    gpio_pull_up(PIN_UP);

    gpio_init(PIN_DOWN);
    gpio_set_dir(PIN_DOWN, GPIO_IN);
    gpio_pull_up(PIN_DOWN);

    gpio_init(PIN_CENTER);
    gpio_set_dir(PIN_CENTER, GPIO_IN);
    gpio_pull_up(PIN_CENTER);

    visible = false;
    selection = settings_get_palette();
}

bool menu_is_visible(void) {
    return visible;
}

void menu_update(void) {
    uint32_t now = to_ms_since_boot(get_absolute_time());
    bool c_up = !gpio_get(PIN_UP);
    bool c_down = !gpio_get(PIN_DOWN);
    bool c_center = !gpio_get(PIN_CENTER);

    if (c_center && !last_center) {
        center_press_start = now;
    } else if (!c_center && last_center) {
        uint32_t duration = now - center_press_start;
        
        if (duration > 500) { 
            // LONG PRESS: open close menu
            visible = !visible;
            last_action = now;
        } else {
            // SHORT PRESS
            if (visible) {
                // if menu is opened apply the selected palette
                palette_apply_no_save(selection);
                visible = false; 
            } else {
                // if menu is closed we check if we have a double click
                if (now - last_short_press_time < DOUBLE_CLICK_THRESHOLD) {
                    settings_save_now();
                    last_short_press_time = 0; 
                } else {
                    last_short_press_time = now;
                }
            }
        }
    }
    last_center = c_center;

    if (!visible) {
        // if menu is not visible, UP/DOWN changes the brightness
        static uint32_t last_btn_time = 0;
        if (now - last_btn_time > 150) {
            if (c_up) { brightness_up(); last_btn_time = now; }
            if (c_down) { brightness_down(); last_btn_time = now; }
        }
    } else {
        // if menu is visible, UP/DOWN changes the palette
        if (c_up && (now - last_action >= SCROLL_DELAY)) {
            if (selection > 0) selection--;
            last_action = now;
        }
        if (c_down && (now - last_action >= SCROLL_DELAY)) {
            if (selection < palette_count() - 1) selection++;
            last_action = now;
        }
        // Auto-close if timeout
        if (now - last_action > MENU_TIMEOUT) visible = false;
    }
}

void menu_draw_overlay(uint8_t buffer_index) {
    if (!visible) return;
    uint8_t *fb = get_framebuffer(buffer_index);
    int count = palette_count();

    draw_rect(fb, START_X - 6, START_Y - 6, 140, (MAX_VISIBLE * 12) + 10, 0);
    
    int first = (selection >= MAX_VISIBLE) ? selection - MAX_VISIBLE + 1 : 0;

    for (int i = 0; i < MAX_VISIBLE && (first + i) < count; i++) {
        int idx = first + i;
        int y_pos = START_Y + (i * 12);
        
        if (idx == selection) {
            
            draw_rect(fb, START_X - 2, y_pos - 2, 132, 11, 2);
            draw_string(fb, START_X, y_pos, palette_name(idx), 0); 
        } else {
            
            draw_string(fb, START_X, y_pos, palette_name(idx), 3);
        }
    }
}